//             OPERADORES LÓGICOS


//um valor tem que dar verdadeiro E o outro também
console.log(3 > 9 && 4 == 4); // && é o AND igual no JAVA
console.log(false && true);

console.log("--------------------------------------------------------------------------------------")
//um valor tem que dar verdadeiro OU o outro tem que dar
console.log(7 > 9 || 0 == 0); // || é o OR igual no JAVA7
console.log(false || true);

console.log("--------------------------------------------------------------------------------------")   
// ! inverte o resultado, se for falso virá verdeiro e virce-versa, igual no JAVA.
console.log(!(2 == 2)) //funcionou com parenteses 
console.log(!true)